#include <iostream>

using namespace std;

int main()
{

    int x;
    x = 5;
    cout << "\n\t | The number stored on x is : " << x;

    double y;
    // float variable declaration and initialization.
    y = 10.234f; // floating point data type
    cout << "\n\t | The floating value of y is: " << y;



    return 0;
}