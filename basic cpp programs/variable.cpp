#include <iostream>

using namespace std;

int main()
{

    int x;
    x = 5;
    cout << "\n\t | The number stored on x is : " << x;

    double y;
    // float variable declaration and initialization.
    y = 1.123456768967767; // floating point data type
    cout << "\n\t | The floating value of y is: " << y;

    float z;
    z = 1.1234567;
    cout << "\n\t |  The decimal value of z is:" << z;
    return 0;
}